package locator.google;

public class GoogleLocatorException extends Exception {

    public GoogleLocatorException() {
    }

    public GoogleLocatorException(String message) {
        super(message);
    }

    public GoogleLocatorException(Throwable cause) {
        super(cause);
    }

    public GoogleLocatorException(String message, Throwable cause) {
        super(message, cause);
    }
}
